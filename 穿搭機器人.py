def calculate_vapor_pressure(temp, rh):
    es = 6.112 * math.exp((17.67 * temp) / (temp + 243.5))
    return es * (rh / 100)

def calculate_feels_like(temp, vapor_pressure, wind):
    return 1.04 * temp + 0.2 * vapor_pressure - 0.65 * wind - 2.7

